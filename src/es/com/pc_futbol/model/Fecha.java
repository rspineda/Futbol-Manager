package es.com.pc_futbol.model;

public class Fecha {

	private int numero;
	private boolean fechaFifa;
	
	//gets and sets:
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public boolean isFechaFifa() {
		return fechaFifa;
	}
	public void setFechaFifa(boolean fechaFifa) {
		this.fechaFifa = fechaFifa;
	}
	
}
